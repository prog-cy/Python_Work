# match statement in python is same as switch statement in C and Java

day = int(input("Enter No. of the week day: "))

match day:

  case 1:
    print('Sunday')
  
  case 2:
    print('Monday')
  
  case 3:
    print('Tuesday')
  
  case 4:
    print('Wednesday')
  
  case 5:
    print('Thursday')
  
  case 6:
    print('Friday')
  
  case 7:
    print('Saturday')
  
  case 8:
    print('Not a week day')
